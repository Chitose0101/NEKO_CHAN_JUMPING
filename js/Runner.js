class Runner extends Entity{
    /*
    走るエンティティの親クラス
    */

    constructor(display_height, display_width) {
        /*
        コントラスタ
        */

        super();

        //初期位置は画面外
        this.set_xy(display_width, display_height - this.height - 1)
    }

    timing_event(level) {
        /*
        フレームごとの処理
        親クラスのオーバーライド
        */
        super.timing_event();
        //x座標の更新
        this.x -= level + 1;
    }  
}

