class Runner extends Entity{
    /*
    走るエンティティの親クラス
    */

    constructor(display_height, display_width, xy) {
        /*
        コントラスタ
        */
        super(display_height, display_width, xy);

        //走る速さ
        this.speed = 0;
    }

    count(level) {
        /*
        フレームごとの処理
        親クラスのオーバーライド
        */
        super.count();

        //速さ更新
        this.speed = level + 1;

        //x座標の更新
        this.x -= this.speed;
    }  
}

